void LED_Reverse_Uart_test(void)
{
    if (HAL_GPIO_ReadPin(KEY1_GPIO_Port, KEY1_Pin) == GPIO_PIN_RESET)
    {
        HAL_Delay(200);
        if (HAL_GPIO_ReadPin(KEY1_GPIO_Port, KEY1_Pin) == GPIO_PIN_RESET)
        {
            char *P_Tx_cmd = "AT+ROLE\r\n";
            HAL_UART_Transmit(&huart3, (uint8_t *)P_Tx_cmd, strlen((const char*)P_Tx_cmd), 200);

            HAL_GPIO_TogglePin(LED1_GPIO_Port, LED1_Pin);
        }
    }
}

